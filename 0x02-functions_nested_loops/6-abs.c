/**
 * _abs - computes the absolute value of an integer
 * int _abs - computes the absolute value of an integer
 *
 *  @n: n is an integer
 *
 * Return: abs value
 */
int _abs(int n)
{
	if (n > 0)
	{
		return (n);
	}
	else if (n < 0)
	{
		return (-n);
	}
	else
	{
		return (n);
	}
}
